import fs from "node:fs";
import path from "node:path";
import { parse } from "csv-parse/sync";

const input = process.argv[2];

if (!input) {
  console.error("Usage: node scripts/import-offer-records.mjs data/offer-records.csv > /tmp/offer-records.sql");
  process.exit(1);
}

const root = process.cwd();
const catalog = JSON.parse(fs.readFileSync(path.join(root, "src/data/catalog.generated.json"), "utf8"));
const universities = Array.isArray(catalog) ? catalog : (catalog.universities ?? []);
const universityLookup = new Map();

for (const university of universities) {
  for (const key of [
    university.id,
    university.slug,
    university.nameEn,
    university.nameZh,
    university.abbreviation,
  ]) {
    if (key) universityLookup.set(normalize(key), university.id);
  }
}

const csv = fs.readFileSync(path.resolve(input), "utf8");
const records = parse(csv, { columns: true, bom: true, skip_empty_lines: true, trim: true });

console.log("-- Generated by scripts/import-offer-records.mjs");
console.log("-- Review before importing into D1.");

for (const row of records) {
  const universityId = resolveUniversityId(row);
  const year = integer(row.year, "year");
  const offerCount = integer(row.offer_count, "offer_count");
  const now = new Date().toISOString();
  const sourceName = row.source_name || "手动整理";
  const sourceSnapshot = row.source_snapshot || "";
  const id =
    row.id ||
    [
      year,
      universityId,
      row.mainland_school_id || "all",
      slug(sourceName),
      sourceSnapshot ? slug(sourceSnapshot) : "record",
    ].join("-");

  const values = [
    id,
    year,
    universityId,
    row.mainland_school_id || null,
    offerCount,
    optionalInteger(row.applicant_count),
    optionalInteger(row.enrolled_count),
    row.degree_level || "undergraduate",
    sourceName,
    row.source_url || null,
    sourceSnapshot || null,
    row.source_kind || "manual",
    row.filters_json || "{}",
    row.notes || "",
    row.created_at || now,
    row.updated_at || now,
  ];

  console.log(`INSERT OR REPLACE INTO university_offer_records (
  id, year, university_id, mainland_school_id, offer_count, applicant_count, enrolled_count,
  degree_level, source_name, source_url, source_snapshot, source_kind, filters_json,
  notes, created_at, updated_at
) VALUES (${values.map(sqlValue).join(", ")});`);
}

function resolveUniversityId(row) {
  if (row.university_id) return row.university_id;
  const matched = universityLookup.get(normalize(row.university_name));
  if (!matched) {
    throw new Error(`Cannot match university: ${row.university_name || "(blank)"}`);
  }
  return matched;
}

function normalize(value) {
  return String(value || "")
    .trim()
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[’']/g, "")
    .replace(/[^a-z0-9\u4e00-\u9fff]+/g, " ");
}

function slug(value) {
  return String(value)
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9\u4e00-\u9fff]+/g, "-")
    .replace(/^-|-$/g, "")
    .slice(0, 80);
}

function integer(value, label) {
  const parsed = Number(value);
  if (!Number.isInteger(parsed) || parsed < 0) {
    throw new Error(`${label} must be a non-negative integer: ${value}`);
  }
  return parsed;
}

function optionalInteger(value) {
  if (value === undefined || value === null || value === "") return null;
  return integer(value, "optional integer");
}

function sqlValue(value) {
  if (value === null || value === undefined) return "NULL";
  if (typeof value === "number") return String(value);
  return `'${String(value).replace(/'/g, "''")}'`;
}
